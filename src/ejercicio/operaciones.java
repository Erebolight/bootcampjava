package ejercicio;

public class operaciones {

	public static int sumar( int a , int b) {
		
		
		return a + b;
	}
	
	public static int restar( int a , int b) {
		
		
		return a - b;
	}
	public static int mul( int a , int b) {
		
		
		return a * b;
	}
	public static int div( int a , int b) {
		
		
		return a / b;
	}
}
